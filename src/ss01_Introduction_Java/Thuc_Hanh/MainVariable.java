package ss01_Introduction_Java.Thuc_Hanh;

public class MainVariable {
    public static void main(String[] args) {
        int i =10;//Khai báo biến i là kiểu int có giá trị là 10;
        float f = 20.5f;// Khai báo biến f là kiểu float có giá trị là 20.5
        double d = 20.5;// Khai báo biến d là kiểu double có giá trị là 20.5
        boolean b = false;
        char c ='a';
        String s = "Hà Nội";
        System.out.println("i = " + i);
        System.out.println("f = " + f);
        System.out.println("d = " + d);
        System.out.println("b = " + b);
        System.out.println("c = " + c);
        System.out.println("s = " + s);
    }
}
